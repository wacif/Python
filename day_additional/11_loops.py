# there are two types of loops "while" and "for" loop

# while loop

x = 0
while(x < 9):
    print("while loop x = ", x)
    x = x+1

# for loop
for x in range(3, 10):
    print("for loop x = ", x)

# arrays
color_names = ["red", "black", "yellow", "blue", "pink", "orange"]
for c in color_names:
    print(c)
